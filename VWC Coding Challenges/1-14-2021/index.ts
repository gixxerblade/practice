//\b(?:(?:2(?:[0-4][0-9]|5[0-5])|[0-1]?[0-9]?[0-9])\.){3}(?:(?:2([0-4][0-9]|5[0-5])|[0-1]?[0-9]?[0-9]))\b

const is_IP = (str: string): boolean => {
  return Boolean(
    str.match(
      /\b(?:(?:2(?:[0-4][0-9]|5[0-5])|[0-1]?[0-9]?[0-9])\.){3}(?:(?:2([0-4][0-9]|5[0-5])|[0-1]?[0-9]?[0-9]))\b/gi
    )
  );
};

console.log(is_IP("198.156.23.5"));
console.log(is_IP("172.16.0.1"));
console.log(is_IP('123.256.123.12'));
