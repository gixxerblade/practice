
enum BHO {
    B = 'B',
    H = 'H',
    O = 'O',
}

const dec_to_bho = (num: number, conversion: BHO[str]) => {
    BHO[str] === 'B' && num.toString(2)
    str === 'H' && num.toString(16)
    str === 'O' && num.toString(10)   
}

